# 35. Create a program that generates Pascal's triangle up to a specified number of rows using list comprehension.
print("Task 35: Pascal's Triangle")
def generate_pascals_triangle(rows):
    triangle = [[1]]
    for i in range(1, rows):
        row = [1] + [triangle[i-1][j] + triangle[i-1][j+1] for j in range(len(triangle[i-1])-1)] + [1]
        triangle.append(row)
    return triangle

rows = 5
pascals_triangle = generate_pascals_triangle(rows)
print(f"Pascal's triangle with {rows} rows:")
for row in pascals_triangle:
    print(row)

# Output:
# Pascal's triangle with 5 rows:
# [1]
# [1, 1]
# [1, 2, 1]
# [1, 3, 3, 1]
# [1, 4, 6, 4, 1]
print("\n")

